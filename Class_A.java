//Class_A will be parent class / superclass
public class Class_A
{
    private int valA;
    protected int valB;
    int valC;
    public int valD;
    
    public void setClassA()
    {
        valA = 10;
        valB = 11;
        valC = 12;
        valD = 13;
       
    }
    public void displayClassA()
    {
       
        System.out.println(+valA +"\t" +valB +"\t" +valC+"\t"+valD +"\t");
        
    }
    
}